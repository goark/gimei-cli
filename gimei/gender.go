package gimei

//Gender is enumeration of gender
type Gender int

const (
	Male Gender = iota
	Female
	Other
)

var genderMap = map[Gender]string{
	Male:   "男性",
	Female: "女性",
	Other:  "その他",
}

func (g Gender) String() string {
	if s, ok := genderMap[g]; ok {
		return s
	}
	return genderMap[Other]
}

